<div class="lecture-text-container">
<p></p><p>Building the calculator is only the first step. Getting traffic is another ballgame altogether. We have identified some growth hacks to spur some initial traffic.<br><br>
</p>
<ul>
    <li><strong>Building virality into the calculator:</strong> Promotion is one thing. But there is a lot you can do while building your calculator that will enhance traffic organically.<br><br><strong></strong>
    <ul>
        <li><strong>Pay with a tweet:</strong> Before (or even after) showing the results, you can ask the user to share the calculator on their social networks as a way to “pay” you. A simple CTA like “Pay With a Tweet” can lead to a good amount of referral traffic.</li>
        <li><strong>One Click Share Links in Email and on LP:</strong> Easily share links with pre-drafted tweets and Facebook/Linkedin posts within the results page and the emails can also help bring in referral traffic. The point is to make it extremely simple and easy for customers to share on their social accounts.</li>
        <li><strong>Clickbait titles</strong>: While clickbait has a negative connotation to it, one can create interesting titles without misleading customers. Misleading clickbaits will eventually lead your customers to lose faith in your brand as they go deeper in your marketing funnel.</li>
    </ul>
    <ul>
        <li><strong>Ask users to recommend the calculator to a friend: </strong>As a part of the lead generation funnel, ask the user to recommend the calculator to a friend. If you ask nicely (and since you’ve added value to them), users are more likely to give the email address of a friend or three who might benefit from the calculator.  <br></li>
    </ul></li>
</ul>
<ul>
    <li><strong><strong>Here is a promotion checklist:</strong></strong></li>
</ul>
<ul>
    <ul>
        <li><strong>Promotion on Social Media: </strong>Of
course you already have conversations on your brand’s social media channels
with your clients, competitors and thought leaders. Begin with adding links to
your calculator’s landing page to highlight what’s new to your audience. 
        </li>
    </ul>
</ul>
<ul>
    <ul>
        <li><strong>Target Groups on LinkedIn and Facebook: </strong>One of the most effective social media channels to approach and promote the
calculator are Facebook and LinkedIn groups. In fact,
        <a href="http://www.topfloortech.com/blog/2015/10/29/yes-social-media-marketing-can-benefit-your-b2b-company">
        LinkedIn is almost 3 times more effective than Facebook and Twitter for B2B
campaign
        </a>s.
For example, ask your team to watch out for conversations about topics you can
turn around to speak about your ROI calculator. Provide answers and chime in
with links to your landing page whenever you find appropriate conversations.
        </li>
    </ul>
</ul>
<ul>
    <ul>
        <li><span></span><strong>PR and Journalists: </strong>Getting others to
promote your content takes a little work, but the payoffs are worth it. Use
press releases as part of a larger marketing campaign, but know that your
campaign might get lost in the crowd if not taken care of. Newswires, just like
search engines prefer press material that is easy to read, engaging and not
loaded with self-promotional links. If you have to, include lighter multimedia
like images. It’s a better to embed video links that serve more info quickly.
But don’t go overboard with SEO terms.
        <br>If your team believes that you have a market-defining calculator at hand, consider pitching it to tech journals. Journalists are always interested in insights that tie into a current trend, solutions that are backed up with relevant, quotable data and content that will be relevant to a large group of people. Make your pitch short and to the point, with two or three useful facts about your content, and then make it easy for them to get in touch with you if they need to follow up.</li>
    </ul>
</ul>
<ul>
    <ul>
        <li><strong> Affiliate and Partnerships: </strong>If
your calculator uses hard data from other marketing sources and research firms,
send out an email to them with a link to your landing page saying something on
the lines of, “hey, we used your public data to create a calculator, and our
leads are coming up with astonishing results.” 
        </li>
    </ul>
</ul>
<ul>
    <ul>
        <li><strong>Influencers: </strong>Your prospects are shrewd
enough to spot a true thought leader from a pushy salesperson. If possible ask
influencers like bloggers to review your tool before you launch your calculator
so that early web content written about you contains some really awesome third
party product validation. This will, in turn, not just attract but also comfort
other outlets to try out your calculator. 
        </li>
    </ul>
</ul>
<ul>
    <ul>
        <li><strong>Retargeting: </strong>Use a retargeting service like
adroll and set it up before you launch your campaign. This will enable you to
re-engage with visitors that were interested enough to visit your campaign page
just once but may not have been motivated enough to click on the calculator. 
        </li>
    </ul>
</ul>
<ul>
    <ul>
        <li><span></span><strong>SEO Tips: </strong>A lot of companies do not invest
in the website they are going to use as a landing page for a marketing
campaign. Not a good move. For your SEO ratings to be high and to ensure that
your marketing tool reaches the right audience, you might want to opt for a
site that Google wants to rank the highest in your niche.
        <ul>
            <li><strong>Content
is king
            </strong>. So make sure there is a lot of relevant content on the pages. Perhaps
you can follow the calculator with a blog post.
            </li>
            <li><span></span><strong>Make sure the URL is expressive</strong>. Make it straightforward and meaningful to your campaign with emphasis on the word ‘calculator’. Google recommends using hyphens (-) instead of underscores (_) in URLs. A phrase with the words that are connected with underscores is treated as a single word by Google’s algorithm.</li>
        </ul></li>
    </ul>
</ul>
<ul>
    <ul>
        <li><strong>Paid Promotion: </strong>If you have the budget, it doesn't hurt to try out Google PPC, Linkedin sponsored posts and Facebook promoted posts etc. The key is to collect data around conversion so you can keep track of the RoI. </li>
    </ul>
</ul>
<p><strong>Finally, remember the qualities of virality that make it transcend technology. Sure, you might email someone a link to something that is going viral, but chances are, your leads are going to talk about your calculator as well. </strong>
</p>
<p><strong>“Viral” is ultimately about your customer base and others marketing for you because you have created something extraordinary, and that’s beyond technology. Tap into that the next time you meet your leads at another marketing expo to begin a conversation. </strong>
</p>
<p><strong>That’s what SaaS startup Motiv did. They targeted their audience (freelance creatives) with a calculator that discerned their ideal hourly and year-long rate. Their calculator’s unsurpassed ability to target incomes boosted SEO, and caught the attention of publications who shared it with enthusiasm.</strong>
</p>
