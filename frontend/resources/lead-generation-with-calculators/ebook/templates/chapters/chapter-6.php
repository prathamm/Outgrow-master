<div class="lecture-text-container">
<p></p><p><strong>Now it's time to fulfill your promise.</strong>
</p>
<p>The customer spent valuable time filling out the inputs, now it's time to give them a worthy output. This is where the user will decide whether to share your calculator. Here’s how some of the most successful calculators ensure success:<br>
</p>
<ul>
    <li><strong>Show Results Upfront</strong>: Don't hide your results. Some calculators tell users to go check their email for the results. This sort of feels like cheating, if you really want to validate the user’s email address, try real time email pinging and validation.</li>
</ul>
<ul>
    <li><strong>Provide Ranges</strong>: In service based businesses like legal and software design, instead of saying it will cost you $10,000, it's a better practice to give yourself some room for error and saying it will cost you between $8,000 to $12,000. This reduces the risk of commitment being misconstrued with such calculators.</li>
</ul>
<ul>
    <li><strong>Show Rich Results</strong>: To make the results richer, you can show multiple outputs instead of just a single number. For example, showing app development cost estimates by region adds to the analysis and makes the value add greater. Or while showing ROI, you can show it in the near term, medium term and long term.<br><br><br><img src="https://lh5.googleusercontent.com/jvUmE1C457lMnO9gxHdsAOunFQHM-MC3wSwk0YGXkAVn1uENX8UnRSnPWe_WEvJnyl1_1b8HaM06qcgfFm3FV6EpN8YX3Z5UE0JTYcxe7cOBZ6-iJxCSljfPYwzncCcmmwr-QJkw" class="img-responsive" width="403" height="275" alt="" style="width: 403px; height: 275px; display: block; margin: auto;"></li>
</ul>
<p style="text-align: center;"><em><span style="font-size: 14px;">This mortgage payment calculator by Yahoo shows up with a graph for an output.</span><br></em>
</p>
<ul>
    <li><strong>Go Graphical</strong>: Or go one step further and show a graphical representation of the results! Sharing rates will go through the roof!<br><br><br><img src="https://lh4.googleusercontent.com/F4WmUpYQHt4leMnkktIDm9QnkGfcgOpBeyeUZYNBZR0P-wQCZE3K5il2lGzAwKCSpYM5a81YabL9OJ47pLUUrx7nhUWJYZuVWn2vYURGJASPTt2KkDjxe_eGH1qPn0P0VxLfqmpm" class="img-responsive" width="385" height="259" alt="" style="width: 385px; height: 259px; display: block; margin: auto;"></li>
</ul>
<p style="text-align: center;"><em><span style="font-size: 14px;">How Talari Networks displays its value proposition clearly in the form of a comparison<br></span></em>
</p>
<ul>
    <li><strong>Show Comparison</strong>: You can even show comparisons. For example, showing a comparison between “how much you would save with [company’s] products” vs [how much you would save with a [competitor’s] product”.<br><br><br><img src="https://lh5.googleusercontent.com/mUTzqv9BrOGpbCxYHAWp5-n0fFEJWgRHHI4n0XlftaXh8G8nzkTXG797C4oV3AFRq92f84LwE98bYF24fBxOj82PZ5OWUbMVYkgZVuWFcfYmfDMYVZlC_6SIcatm46gYwp2CQKdc" class="img-responsive" width="399" height="196" alt="" style="width: 399px; height: 196px; display: block; margin: auto;"></li>
</ul>
<p style="text-align: center;"><i><span style="font-size: 11px;"><em><span style="font-size: 14px;">VenturePact's cost calculator shows the breakdown of each cost component.<br></span></em></span></i>
</p>
<ul>
    <li><strong>Show Breakdown</strong>: Instead of simply giving a number as a result, you can even try to showcase the cost breakdown of each of the line items. This can allow the user to see how each component impacts the price.</li>
</ul>
<ul>
    <li><strong>Direct Them Further</strong>: Now that you have added value to the user, you’ve earned the right to direct them to your website or sales page. Design the end-screen of your calculator to display CTAs directing the user to other sections of your site. You can serve up articles from your marketing blog, published whitepapers and informational videos to provide content depth to your campaign or you can give them an opportunity to schedule call directly.</li>
</ul>
<ul>
    <li><strong>Have a Disclaimer:</strong> In order to negate any legal risks, do remember to mention that all results are not binding in any way and are simply estimates to be used for educational purposes.</li>
</ul><p></p>
