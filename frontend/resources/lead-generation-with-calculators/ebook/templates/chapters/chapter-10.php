<p></p><p>Alright! So you now have the lead! Good job! Now is where it gets even more tricky! How do you handle the leads and convert them? Here’s what we found:<br>
</p>
<ul>
  <li><strong>Build Personas: </strong>It’s a good idea to build personas around your leads. This is to personalize your marketing campaign based on your user's’ identity and online behavior. Transactional emails, or emails that are sent to users based on tracked online behavior, outperform non-transactional emails by 17 percent.</li>
</ul>
<ul>
  <li><strong>Lead Scoring: </strong>Following up and nurturing your leads can be a tricky and exhausting endeavor. For instance, it might be difficult for your sales staff to know exactly which leads will convert successfully into customers. So, it can be tempting to not follow up on a lead just based on an assumption. Such unpredictability has pushed a lot of businesses to spend all their time and energy on all leads. But that is an inefficient way to follow up on leads. Ask yourself the following questions:</li>
</ul>
<ul>
  <ul>
    <li>What, according to your metrics, is a good lead?</li>
    <li>Which of the leads still need more nurturing? </li>
    <li><span></span>Where should your resources go? </li>
    <li><span></span>What data do you use to draw conclusions on your leads? </li>
    <li><span></span>The more you know about your leads, the easier it will be to answer these questions.</li>
  </ul>
</ul><span style="line-height: 1.6em; color: rgb(63, 63, 63); background-color: initial;">
<ul>
  <li><strong>Get Personal: </strong>Today’s marketing automation programs make the lead personalization process scalable. This process involves using merge tags to pull personal information from individuals profiles and display said information when the prospect opens an email or visits a landing page. Start by personalizing emails and landing pages. Include the recipient’s name, sender name (of course) and images, messaging, and CTA based on your lead’s persona or behavior. If you haven’t ever done this, <a href="http://pages2.marketo.com/rtp-website-cheat-sheet-targetmarketingnewsletter-april2014.html?source=Email-Sponsored&amp;comment=targetmarketingnewsletter-emailsponsored-april2014&amp;_kk=&amp;offer=RTP">here</a>’s a personalization cheat sheet from Marketo to help you jumpstart.</li>
</ul>
<ul>
  <li><strong>Hand Off Leads To Sales: </strong>Don’t forget to surface prospect insights to your sales team. Show them the prospect's actual calculator inputs so they understand the buyer's’ unique parameters and buying situation. This helps accelerate deals by providing the buyer with a great experience by meeting his unique expectations, and information surfaced in an online experience doesn’t have to be rehashed in a scripted sales experience.</li>
</ul>
<ul>
  <li><span></span><strong>Sales Touch: </strong>A member of the sales team can then follow up with your lead prospects (preferably over phone). Your sales reps can discuss the results in detail or lay out how your product can reduce their pain points. <strong>Ideally, the sales touch should be focused on the results of the calculator and not selling the product. </strong></li>
</ul></span>
<p style="margin-left: 60px;"><span style="color: rgb(227, 108, 9);"><span style="color: rgb(63, 63, 63);"><strong>Pro Tip</strong>: </span><span style="color: rgb(63, 63, 63);">According to <a href="http://blog.insidesales.com/lead-management/lead-response-management-infographic/"><span style="color: rgb(227, 108, 9);"><u></u></span></a><u><a href="http://blog.insidesales.com/lead-management/lead-response-management-infographic/"><u>research</u></a></u></span></span><span style="color: rgb(63, 63, 63);">, the best time to make first contact with your leads is within five minutes of them completing your lead generation form. And, the best time to make a follow-up call is between 4-5 pm on Wednesdays and Thursdays.</span>
</p>
<ul>
  <li><strong><span style="color: rgb(227, 108, 9);"><strong><span style="color: rgb(227, 108, 9);"><span style="color: rgb(227, 108, 9);"><strong><strong><span style="color: rgb(63, 63, 63);">Cadence: </span></strong></strong></span></span></strong></span></strong>Cadence is the difference between mindlessly bombarding your leads with campaign emails and a carefully crafted email schedule. Basically, cadence is NOT how frequently you send out your marketing emails. It’s to track your leads’ online behavior and analyses their purchasing habits, and mindfully build an email schedule around that information. You must also ensure to:</li>
</ul>
<ul>
  <ul>
    <li><span style="color: rgb(49, 133, 155);"><em><span style="color: rgb(63, 63, 63);">Cross sell other educational content around your product in your email.</span></em></span></li>
    <li><span style="color: rgb(49, 133, 155);"><em><span style="color: rgb(63, 63, 63);">You can even include your calculator again within the email reminding your leads where they began the relationship with you.</span></em></span></li>
    <li><span style="color: rgb(49, 133, 155);"><em><span style="color: rgb(63, 63, 63);">Ask your leads to share it with their peers if they find your product interesting. You can even build triggers into your email campaigns; if a prospect shows interest in an email, then it triggers the next email providing them more whitepapers and webinars around your product.</span></em></span></li>
  </ul>
</ul>
<p>How do you know if your efforts are working in this direction? There are several metrics for engagement beyond just email click rate – look at email replies, unsubscribes, landing page conversions, and even sales if you're using a closed-loop marketing software.
</p>
