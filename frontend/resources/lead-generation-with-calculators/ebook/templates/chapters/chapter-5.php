<div class="lecture-text-container">
<p></p><p>Once the user has clicked on the CTA and entered the actual calculator, the interface should be such that the user goes through the entire calculation without bouncing off in the middle.
</p>
<p>Here are some of the best practices that we have found so far:
</p>
<ul>
    <li><strong>Five to Eight Questions Max:</strong> The magic number of input requirements / questions lies somewhere between five and eight. Anything more than that, and you’re risking a high bounce rate.</li>
</ul>
<p style="margin-left: 60px;"><strong>Pro tip:</strong> In case you do want to include up to ten questions, have them divided into sections so you don’t scare away your users with the length of the questionnaire.
</p>
<ul>
    <li><strong>Use Checkboxes and Sliders:</strong> Avoid asking for text inputs. Because they are ambiguous and they just need too much thinking and time from the user. Say you’re asking someone their height, provide a slider with pre-standardized units instead of letting the user imagine which units she should use.</li>
</ul>
<ul>
    <li><strong>Display 1-2 Questions at a Time</strong>: To avoid confusing the user, stick to one question at a time unless the calculator requires for real time variable adjustment and calculations.</li>
</ul>
<ul>
    <li><span></span><strong>Include Help Text</strong>: Wherever required, add descriptions to your question to make things clearer. Say you’re asking someone what their height is and all you need is an approximate number, not the exact height. So it would help to say “Enter an approximate number” so as to save the user's time (in case someone decides to get a tape and measure his exact height).<br></li>
    <li><strong>Include a Progress Bar</strong>: It helps conversion if your users can track their progress. Once your users know that they have completed over 50% of the questions, they are more likely to finish.</li>
</ul>
<ul>
    <li><strong>Usability &gt; Accuracy: </strong>The bottom line is it is more important to make the calculator easy to use than to make it one hundred percent accurate. Take a closer look at the number of steps the calculator is taking to arrive at a conclusion. Can this be reduced? Have you adopted the fastest and the shortest route to a solution? You can begin by asking your team to create models based on assumptions made. </li>
</ul><p></p>
