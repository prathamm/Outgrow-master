<p></p><p><strong>Lead generation is where leads convert into real business.</strong>
</p>
<p>Optimizing this is what makes or breaks your campaign strategy. Don’t believe us? Expedia <a href="http://uxmovement.com/resources/the-12-million-optional-form-field/"><u>gained $12 million in profit </u></a>by removing just one redundant field from a form.
</p>
<p>Asking your leads for their contact info is tricky. Your use case dictates where you would like to place the lead gen form (where you ask for customer info). Ideally, place your lead gen form before the calculator results are shown to maximize your conversion and number of leads generated. Simply said, your leads will be more willing to give away their email IDs after they have invested enough time filling out the calculator.
</p>
<p>That being said, if your aim is to increase brand awareness, then you can let go of lead generation completely. Because it’s not always about the sales pitch. For instance, the globally respected The New York Times, decided to create <a href="http://www.nytimes.com/interactive/2014/your-money/student-loan-repayment-calculator.html"><u>a student loan calculator </u></a>to connect with its young professional and student readers. The NYT was not selling a service related to the topic but offering a free product with the intention of deepening their relationship with their audience.
</p>
<p><img src="https://lh6.googleusercontent.com/xvw8LnONuzHNfLcCP9Wj3h9mkCUSp4KnpX59i6EbtVnYityXJF5p9Tk5u1rPe2pToV-Z2vb4fix58sp1ppgNOn7rOQAmtwFyjuEmTxtkYHtNBAMvwMCdAej369bQL7z9A2hecRQ" alt="Student Loan Calculator The New York Times.png">
</p>
<p>But given that most calculators will need to have a strong lead generation mechanism, here’s what you can do to maximize conversion<br><br>
</p>
<p><img src="https://lh4.googleusercontent.com/Ia6o3AdflxE-PP4mme7Pc90f99Ka0_kzoormeRbm-ZwNVAl6NLZgkuWDujPDgaTQ-nsjxmaYG-JsUWldaDKZjeB_la2eXuxpNvFuIkeVBoFxylO00LdHfo-2tehyznGTU3_6Xo4" alt="" style="display: block; margin: auto;">
</p>
<ul>
    <li><strong>Don’t Make it Scary Long</strong>: For example, you have to admit this lead gen form looks scary because of the sheer amount of data it asks you for. The rule of thumb is anywhere between one to four form fields. Anything beyond that and you’re risking bounces.</li>
</ul>
<ul>
    <li><strong>What to ask?</strong> Here is our recommendation:<br><br>
    <ol>
        <li><strong>Full Name</strong>: It's easy and expected</li>
        <li><strong>Email or Phone</strong>: Do you really need both?</li>
        <li><strong>Company Website</strong>: Most questions will get answered here. </li>
        <li><strong>Industry or Position</strong>: Helps you gauge the decision making power of your lead and then build a buyer persona around it.</li>
        <li><strong>Industry Specific Questions</strong>: If you really need to, ask them but make them check boxes or sliders rather than text input.</li>
    </ol></li>
</ul>
<ul>
    <li><strong>Validations</strong>: Try validating the email ID in the input box itself. Using email verification tools you can ensure that the email entered is a real email and not a fake one. Similar validations can be added for phone numbers as well.</li>
</ul>
<ul>
    <li><strong>Ensure Autofill Works</strong>: Work with your developer to ensure that the calculator accepts autofill. Forms that auto populate themselves are more likely to be filled and submitted.</li>
</ul>
<ul>
    <li><strong>User Experience is Key</strong>: Take care of the surrounding space on the page that contains the form. Give the form some room to “breathe”, and use directional cues to highlight important inputs. It’s a good idea to highlight elements that reinforce what marketers call the ‘statement of benefit’ - basically what your lead will get in return for filling the form.</li>
</ul>
<p style="margin-left: 60px;"><strong>Pro tip</strong>: The thumb rule is to design, the page so it passes the blink test, which is roughly six seconds from the moment the visitor enters it. Minimize friction by avoiding dissonant colors, cluttered text, unplanned and distracting navigation, and parasitic CTA that distract the u user from your main goal: submission.
</p><p></p>
